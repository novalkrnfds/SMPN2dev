<p style="text-align:justify;"><span class="dropcap">S</span>SMP Negeri 2 Sungai Penuh sejak berdiri tanggal 25 Mei 1960 bernama SGB sampai berubah menjadi SMP Negeri 2 Sungai Penuh, 
				Sejak berdirinya Tahun 1960 sampai sekarang SMP Negeri 2 Sungai Penuh  berkedudukan di Sungai Penuh ibu kota Kabupaten Kerinci sekarang berubah nama Kota Sungai Penuh, tepatnya di Jl.Jend. Sudirman  Kecamatan Sungai Penuh Kota Sungai Penuh. 
				Berdirinya SMP Negeri 2 Sungai Penuh ini atas kerjasama seluruh masyarakat Kecamatan Sungai Penuh untuk mendesak Pemerintah Daerah Tingkat II Kabupaten Kerinci, 
				karena masyarakat memandang bahwa Sekolah Lanjutan Tingkat Pertama di Kabupaten Kerinci tidak akan mungkin dapat menampung semua tamatan Sekolah Dasar, pada tahun 1960, Tamatan Sekolah Dasar memang sangat banyak. 
				Jadi cukup beralasan ketika masyarakat mengharap adanya Sekolah Lanjutan Tingkat Pertama di Kecamatan Sungai Penuh.
				SMP Negeri 2 Sungai Penuh kini telah mengalami beberapa kemajuan yang sangat signifikan, SMP Negeri Sungai Penuh telah memiliki XXIV Ruang Kelas, X Ruang Lab, dan Lapangan Olah Raga. 
				Dalam Perjalanan SMP Negeri 2 Sungai Penuh yang berdiri tahun 1960 terus berbenah dibawah kepemimpinan kepala sekolah yang telah beberapa kali mengalami pergantian hingga kini telah memiliki 9 (sembilan) orang kepala sekolah. 
				Kepala Sekolah tersebut sama-sama mempunyai visi dan misi dalam membangun SMP Negeri 2 Sungai Penuh kearah kemajuan dalam mencapai tujuan pendidikan nasional sesuai dengan undang-undang dasar 1945 yaitu "untuk mencerdaskan kehidupan bangsa."</div>
